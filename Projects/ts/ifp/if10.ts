let angka1: number = 10;
let angka2: number = 3;
let operator: string = "/";

if (operator === "+") {
   console.log(`${angka1} + ${angka2} = ${angka1} + ${angka2}`);
} else if (operator === "-") {
   console.log(`${angka1} _ ${angka2} = ${angka1} + ${angka2}`);
} else if (operator === "*") {
   console.log(`${angka1} * ${angka2} = ${angka1} + ${angka2}`);
} else if (operator === "/") {
   console.log(`${angka1} / ${angka2} = ${angka1} + ${angka2}`);
} else {
  console.log("operator tidak tersedia")
}

