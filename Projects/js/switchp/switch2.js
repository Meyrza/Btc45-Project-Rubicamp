let pilihan = 2;

switch (pilihan) {
    case 1:
        console.log("Anda memilih: Mie Ayam");
        console.log("Harga: Rp 12000");
        break;
    case 2:
        console.log("Anda memilih: Nasi Goreng");
        console.log("Harga: Rp 15000");
        break;
    case 3:
        console.log("Anda memilih: Sate Ayam");
        console.log("Harga: Rp 20000");
        break;
    default:
        console.log("Pilihan tidak valid! Silakan pilih 1, 2, atau 3.");
}
